CMD_QUICK_ACTION = 0x21
CMD_EXECUTE = 0x22
CMD_PAGE_NEXT = 0x23
CMD_PAGE_PREV = 0x24
CMD_INPUT = 0x25

# CH_STATUS
ST_HEARTBEAT = 0x30
ST_DEVICE_UP = 0x31
ST_DEVICE_DN = 0x32
ST_INFO = 0x33

# CH_STREAM
STR_DATA = 0x40
STR_CLEAR = 0x41
STR_MIRROR = 0x42

# Mirror draw ops (payload of STR_MIRROR)
MOP_FILL = 0x01
MOP_PIXEL = 0x02
MOP_RECT = 0x03
MOP_HLINE = 0x04

# Mirror modes
MIRROR_TERMINAL = 0x00
MIRROR_GRAPHICAL = 0x01

# CH_OTA
OTA_BEGIN = 0x50
