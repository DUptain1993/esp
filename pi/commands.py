# Addressing
DEVICE_BROADCAST = 0xFF
DEVICE_SELF = 0x01

# Comms payload flag bits ([FLAGS][MSG_LEN][PAD_LEN][BODY])
FLAG_RLE = 0x01
FLAG_DELTA = 0x02
FLAG_ENC = 0x04

# CH_CONTROL
CMD_NOISE = 0x00
CMD_PING = 0x10
CMD_PONG = 0x11
